package loops;

public class WhileLoopPractice {
    public static void main(String[] args) {
        int x=0;
        while (x<10){
            System.out.println("Aaliyah Maqsurat"+x);
            x++;
        }
        int y=5;
        while (y>-10){
            System.out.println("Manha Mahrosh" +y);
            y--;
        }
        int m =0;
        while (m<4){
            System.out.println("Tuhfa Maisara"+m);
            m++;
        }
        int t = 3;
        while(t>-4){
            System.out.println("Tahmina Hoque");
            t--;
        }
        int n=2;
        while (n<4){
            System.out.println("Shadman Hoque"+n);
            n++;
        }

    }
}
