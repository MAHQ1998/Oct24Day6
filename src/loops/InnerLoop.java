public class InnerLoop {// nested loop or inner loop, Loop inside a loop.
    public static void main(String[] args) {
        for(int i=0;i<5;i++) {
            System.out.println("Today is Wednesday" + i);

        for(int j=0;j<2;j++) {
            System.out.println("Today is Thursday" + j);
        }


          }

        }

    }

