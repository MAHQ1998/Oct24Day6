package loops;

public class ForLoop {
    public static void main(String[] args) {


        // for is the Key word(--body start
        // int- is the data type
        // i is the variable and variable start
        //< variable end point
        //i variable and ++ incremental manner
        // your result from print line
        for (int i = 0; i < 15; i++) {
            System.out.println("Yes We Can" + i);
        }
        //decremental manner
        for (int i = 0; i > -5; i--) {
            System.out.println("Need to go to college" + i);
        }

        for(int i=0; i<-2;i--){
            System.out.println("I Voted by mail"+i);
        }
        for(int i=0; i>-5; i--){
            System.out.println("I Vote NJ"+i);
        }

    }

}