package loops;

public class ForLoop3 {
    public static void main(String[] args) {
        for(int i=3; i> -3; i--){
            System.out.println("Fall is the best season"+i);
        }
        for(int i= 0;i < 8; i ++){
            System.out.println("let's go long drive" +i);
        }
        for ( int i=0;i>-10; i--){
            System.out.println("it's Foggy Today" +i);
        }
    }



}
