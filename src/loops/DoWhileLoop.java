package loops;

public class DoWhileLoop {
    public static void main(String[] args) {

        int x=0;
        do {
            System.out.println("Lava");
            x++;
        }
        while (x<6);
        int y=0;
        do{
            System.out.println("Papa");
            y++;
        }
        while (y<4);
      }
    }

