package loops;

public class ConditionalForLoop2 {
    public static void main(String[] args) {

        for(int i = 0; i>-2; i--){
            if(i==-1){
                System.out.println("I will wash my car");
            }else{
                System.out.println(" I will not wash my car");
            }
        }
    }
}
